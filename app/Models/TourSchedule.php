<?php

namespace App;

use Illuminate\Database\Eloquent\Model;

class TourSchedule extends BaseModel
{
    //
	protected $table = 'tour_schedule';
	protected $fillable = [
							'departure', 
							'arrival', 
							'currency', 
							'original_price', 
							'discounted_price', 
							'tour_id'
						];
	protected $dates = ['departure', 'arrival'];

	// ----------------------------------------------------------------------
	// BOOT
	// ----------------------------------------------------------------------
	static function boot()
	{
		parent::boot();
		Static::observe(new TourScheduleObserver);
		Static::observe(new BelongsToTourObserver);
	}

	// ----------------------------------------------------------------------
	// RELATIONS
	// ----------------------------------------------------------------------
	function tour()
	{
		return $this->belongsTo(__NAMESPACE__  .'\Tour');
	}

	// ----------------------------------------------------------------------
	// SCOPES
	// ----------------------------------------------------------------------

	// ----------------------------------------------------------------------
	// MUTATORS
	// ----------------------------------------------------------------------

	// ----------------------------------------------------------------------
	// ACCESSORS
	// ----------------------------------------------------------------------

	// ----------------------------------------------------------------------
	// ACCESSORS
	// ----------------------------------------------------------------------

}
