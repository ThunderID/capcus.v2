<?php

namespace App;

use Illuminate\Database\Eloquent\Model;

class TourSchedule extends BaseModel
{
    //
	protected $table = 'tour_schedule';
	protected $fillable = [
							'depart', 
							'return', 
							'currency', 
							'original_price', 
							'discounted_price', 
						];
	protected $dates = ['depart', 'return'];

	// ----------------------------------------------------------------------
	// BOOT
	// ----------------------------------------------------------------------
	static function boot()
	{
		parent::boot();
		Static::observe(new TourScheduleObserver);
	}

	// ----------------------------------------------------------------------
	// RELATIONS
	// ----------------------------------------------------------------------
	function tour()
	{
		return $this->belongsTo(__NAMESPACE__  .'\Tour');
	}

	// ----------------------------------------------------------------------
	// SCOPES
	// ----------------------------------------------------------------------

	// ----------------------------------------------------------------------
	// MUTATORS
	// ----------------------------------------------------------------------

	// ----------------------------------------------------------------------
	// ACCESSORS
	// ----------------------------------------------------------------------

	// ----------------------------------------------------------------------
	// ACCESSORS
	// ----------------------------------------------------------------------

}
