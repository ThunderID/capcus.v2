<?php

namespace App;

use Illuminate\Database\Eloquent\Model;

class Image extends BaseModel
{
    //
	protected $table = 'images';
	protected $fillable = [
							'path', 
							'name',
							'title',
							'description', 
						];

	// ----------------------------------------------------------------------
	// BOOT
	// ----------------------------------------------------------------------
	static function boot()
	{
		parent::boot();
		Static::observe(new ImageObserver);
	}

	// ----------------------------------------------------------------------
	// RELATIONS
	// ----------------------------------------------------------------------
	function imageable()
	{
		return $this->morphTo();
	}

